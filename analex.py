# Função para processar o arquivo e gerar a saída com tratamento de erro
def processar_arquivo(nome_arquivo):
    with open(nome_arquivo, 'r', encoding='utf-8') as arquivo:
        for num_linha, linha in enumerate(arquivo, start=1):
            # Processa a linha com o autômato
            try:
                output = moore.get_output_from_string(linha.strip())
                
                # Verifica se o autômato entrou no estado de erro
                if 'q_error' in output:
                    # Encontra o caractere inválido
                    for i, char in enumerate(linha.strip(), start=1):
                        # Verifica se o caractere leva ao estado de erro
                        next_state = moore.transitions[moore.current_state].get(char, 'q_error')
                        if next_state == 'q_error':
                            print(f"Erro[{num_linha:02}][{i:02}]: Caracter inválido: {char}")
                            break
                else:
                    # Se não houve erro, imprime a saída normal em uma linha
                    for token in output.split():
                        print(token)  # Imprime cada token em uma linha separada
            except KeyError:
                # Ignora KeyError (caracteres inválidos)
                pass

